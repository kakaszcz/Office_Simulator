abstract public class Agent {
    int x;
    int y;
    double wydajnosc;
    double doswiadczenie;

    public Agent(int x, int y, double wydajnosc, double doswiadczenie) {
        this.x = x;
        this.y = y;
        this.wydajnosc = wydajnosc;
        this.doswiadczenie = doswiadczenie;
    }
}
